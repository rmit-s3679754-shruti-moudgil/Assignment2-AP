    public void reloadProfiles(){
        profiles = miniNet.getAllProfiles();
        profileListModel.clear();
        selectedProfileModel.removeAllElements();
        selectedProfileModel.addElement("Select a Profile");
        for(Profile p : profiles){
            profileListModel.addElement(p.toString());
            selectedProfileModel.addElement(p.toString());
        }
    }
    
    public void reloadConnections(){
        connections = miniNet.getAllConnections();
        connectionListModel.clear();
        for(Connection c : connections){
            connectionListModel.addElement(c.toString());
        }
    }
    
