    private void btnDeleteProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProfileActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jcmbxSelected.getSelectedIndex();
        if(selectedIndex <= 0){
            JOptionPane.showMessageDialog(this, "No profile selected!");
        }
        else{
            Profile p = profiles.get(selectedIndex - 1);
            try{
                miniNet.removeProfile(p);
                reloadProfiles();
                reloadConnections();
                JOptionPane.showMessageDialog(this, "Profile deleted from MiniNet.");
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
        }
    }//GEN-LAST:event_btnDeleteProfileActionPerformed

