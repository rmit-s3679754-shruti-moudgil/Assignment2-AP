    private void btnViewProfileActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedIndex = jcmbxSelected.getSelectedIndex();
        if(selectedIndex <= 0){
            JOptionPane.showMessageDialog(this, "No profile selected!");
        }
        else{
            Profile p = profiles.get(selectedIndex - 1);
            String info = "Name " + p.getName() + ":\n" + "Age " + p.getAge() +  ":\n";
            try{
                info += "Children: ";
                if(p.getChildren().length > 0){
                    Profile[] children = p.getChildren();
                    for(Profile child : children){
                        info += "- " + child.getName()+"\n";
                    }
                }
                else {
                    info += "None";
                }
            }
            catch(Exception e){
                info += "None" +"\n";
            }
            try {
                info += "Parents: ";
                if(p.getParents().length >0){
                    Profile[] parents = p.getParents();
                    for(Profile parent : parents){
                        info += "- " + parent.getName()+"\n";
                    }                
                }
                else {
                    info += "None";
                }             
            }
            catch(Exception e){
                info += "None" +"\n";
                System.out.println(e);
            }

            JOptionPane.showMessageDialog(this, info);
        }        
    }

